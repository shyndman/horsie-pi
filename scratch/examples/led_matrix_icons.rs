#![no_std]
#![no_main]
#![feature(type_alias_impl_trait)]

use defmt_rtt as _;
use embassy_executor::Spawner;
use embassy_rp::{
    bind_interrupts,
    gpio::{Level, Output},
    peripherals::PIO0,
    pio::{InterruptHandler, Pio},
};
use embassy_time::{Duration, Ticker};
use panic_probe as _;
use rgb::RGB8;
use scratch::ws2812::{
    effects::EffectFrameProvider, Ws2812Chain, Ws2812FrameProvider,
};

bind_interrupts!(struct Irqs {
    PIO0_IRQ_0 => InterruptHandler<PIO0>;
});

#[embassy_executor::main]
async fn main(_spawner: Spawner) {
    let p = embassy_rp::init(Default::default());

    let mut led = Output::new(p.PIN_25, Level::High);
    led.set_high();

    let Pio {
        mut common, sm0, ..
    } = Pio::new(p.PIO0, Irqs);

    // Number of LEDs in the string
    const LED_N: usize = 16 * 16;
    let mut effect_frames = IconFrameProvider::<LED_N>::new();
    let mut led_chain = Ws2812Chain::<PIO0, 0, LED_N>::new(
        &mut common,
        sm0,
        p.DMA_CH0,
        p.PIN_16,
        &effect_frames,
    )
    .await;
    led_chain.brightness = 0xC0;

    let mut ticker = Ticker::every(Duration::from_secs(3));
    loop {
        led_chain.draw(&effect_frames).await;
        ticker.next().await;
        effect_frames.advance();
    }
}

pub struct IconFrameProvider<const LED_N: usize> {
    index: usize,
    frame: [RGB8; LED_N],
}

impl<const LED_N: usize> IconFrameProvider<LED_N> {
    pub fn new() -> Self {
        let mut icon_frame = Self {
            index: 0,
            frame: [RGB8 { r: 0, g: 0, b: 0 }; LED_N],
        };
        icon_frame.fill_frame();
        icon_frame
    }

    fn fill_frame(&mut self) {
        let icon = ICONS[self.index % ICONS.len()];

        let frame_index = |x: usize, y: usize| -> usize {
            if x % 2 == 0 {
                x * 16 + y
            } else {
                x * 16 + (16 - y - 1)
            }
        };

        for y in 0..8 {
            for x in 0..8 {
                self.frame[frame_index(x * 2, y * 2)] = icon[y * 8 + x];
                self.frame[frame_index(x * 2 + 1, y * 2)] = icon[y * 8 + x];
                self.frame[frame_index(x * 2, y * 2 + 1)] = icon[y * 8 + x];
                self.frame[frame_index(x * 2 + 1, y * 2 + 1)] = icon[y * 8 + x];
            }
        }
    }
}

impl<const LED_N: usize> EffectFrameProvider for IconFrameProvider<LED_N> {
    fn advance(&mut self) {
        self.index += 1;
        self.fill_frame();
    }
}

impl<const LED_N: usize> Ws2812FrameProvider<LED_N> for IconFrameProvider<LED_N> {
    fn frame_colors(&self) -> [RGB8; LED_N] {
        self.frame
    }
}

